    public void cargarCarrito() {
        String[] cabecera = {"Titulo ", "Precio "};
        String[][] datosLineaVenta = new String[carrito.getVentasCarrito().size()][2];
        for (int i = 0; i < carrito.getVentasCarrito().size(); i++) {
            datosLineaVenta[i][0] = carrito.getVentasCarrito().get(i).getLibroLineaVenta().getTitulo();
            datosLineaVenta[i][1] = String.valueOf(carrito.getVentasCarrito().get(i).getLibroLineaVenta().getPrecio());
        }
        jTable.setModel(new javax.swing.table.DefaultTableModel(datosLineaVenta, cabecera));

        txtPrecioFinal.setText(String.valueOf(this.carrito.calcularPrecioFinalCarrito()));
    }

